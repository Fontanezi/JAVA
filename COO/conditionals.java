class Conditionals {
    public static void main(String[] args) {
        int x = 10, y = 10;
        if (x == y) {
            System.out.println("X é igual a Y!");
        } else{
            System.out.println("X não é igual a Y!");
        }

        // Shorthand: String result = (x == y) ? "X é igual a Y!" : "X não é igual a Y!";

        // For loops, While loops and Do...While loops are the same as in C.

    }
}
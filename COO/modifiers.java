// ACCESS MODIFIERS

/*
 * FOR CLASSES:
 * 
 * public - The class is accessible by other classes.
 * default - Only used by classes in the same package.
 * 
 * FOR ATTRIBUTES, METHODS AND CONSTRUCTORS
 * 
 * public - All classes can access.
 * private - Only accessible within the declared class.
 * default - Only accessible in the same package
 * protected - Accessible in the same package and subclasses.
 * 
 * 
 */

// NON-ACCESS MODIFIERS

/*
 * FOR CLASSES:
 * 
 * final - Class cannot be inherited by other classes.
 * abstract - Class cannot be used to create objects
 * 
 * 
 * FOR ATTRIBUTES, METHODS AND CONSTRUCTORS
 * 
 * final - Can't modify (const)/
 * static - Attributes and methods belongs to the class, rather than an object.
 * abstract - Can only be used in an abstract class, and can only be used in
 * methods.
 * transient - Attributes and methods are skipped when serializing the object
 * containing them.
 * 
 * synchronized - Methods can only be accessed by one thread at a time
 * 
 * volatile - The value of an attribute is not cached thread-locally, and is always read from the "main memory".
 * 
 * 
 * 
 * 
 * 
 * 
 */